/* USER CODE BEGIN 1 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	//编码器A
	if(__HAL_TIM_IS_TIM_COUNTING_DOWN(&TIM_EncoderHandle1))
	//下溢 
		Encoder_Overflow_Count_A--;
	else
	//上溢 
		Encoder_Overflow_Count_A++;
	
	//编码器B
	if(__HAL_TIM_IS_TIM_COUNTING_DOWN(&TIM_EncoderHandle2))
		Encoder_Overflow_Count_B--;
	else
		Encoder_Overflow_Count_B++;
}
